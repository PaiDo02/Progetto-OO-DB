package ClassiTabelle;

public class Tartaruga {
	private String Nome;
	private String IDTartaruga;
	private String Targhetta;
	private String FKAmmissione;
	
	//SETTER AND GETTER
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getIDTartaruga() {
		return IDTartaruga;
	}
	public void setIDTartaruga(String iDTartaruga) {
		IDTartaruga = iDTartaruga;
	}
	public String getTarghetta() {
		return Targhetta;
	}
	public void setTarghetta(String targhetta) {
		Targhetta = targhetta;
	}
	public String getFKAmmissione() {
		return FKAmmissione;
	}
	public void setFKAmmissione(String fKAmmissione) {
		FKAmmissione = fKAmmissione;
	}
}
