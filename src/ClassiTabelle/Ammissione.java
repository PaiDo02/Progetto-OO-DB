package ClassiTabelle;

public class Ammissione {
	
	private String Centro;
	private String Riammissione;
	private String IDAmmissione;
	private String DataDiAmmissione;
	
	//GETTER AND SETTER
	public String getCentro() {
		return Centro;
	}
	public void setCentro(String centro) {
		Centro = centro;
	}
	public String getRiammissione() {
		return Riammissione;
	}
	public void setRiammissione(String riammissione) {
		Riammissione = riammissione;
	}
	public String getIDAmmissione() {
		return IDAmmissione;
	}
	public void setIDAmmissione(String iDAmmissione) {
		IDAmmissione = iDAmmissione;
	}
	public String getDataDiAmmissione() {
		return DataDiAmmissione;
	}
	public void setDataDiAmmissione(String dataDiAmmissione) {
		DataDiAmmissione = dataDiAmmissione;
	}

}
