package ClassiTabelle; 

public class DocumentazioniCartelleCliniche {
	
	private String IDDocumentazione;
	private String IDTartaruga;
	private String IDCartellaClinica;
	
	//GETTER AND SETTER
	public String getIDDocumentazione() {
		return IDDocumentazione;
	}
	public void setIDDocumentazione(String iDDocumentazione) {
		IDDocumentazione = iDDocumentazione;
	}
	public String getIDTartaruga() {
		return IDTartaruga;
	}
	public void setIDTartaruga(String iDTartaruga) {
		IDTartaruga = iDTartaruga;
	}
	public String getIDCartellaClinica() {
		return IDCartellaClinica;
	}
	public void setIDCartellaClinica(String iDCartellaClinica) {
		IDCartellaClinica = iDCartellaClinica;
	}

}
