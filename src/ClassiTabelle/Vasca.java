package ClassiTabelle;

public class Vasca {
	
	private String CodiceVasca;
	private String Tipo;
	private String Lunghezza;
	private String Larghezza;
	private String Profondità;
	
	public String getCodiceVasca() {
		return CodiceVasca;
	}
	public void setCodiceVasca(String codiceVasca) {
		CodiceVasca = codiceVasca;
	}
	public String getTipo() {
		return Tipo;
	}
	public void setTipo(String tipo) {
		Tipo = tipo;
	}
	public String getLunghezza() {
		return Lunghezza;
	}
	public void setLunghezza(String lunghezza) {
		Lunghezza = lunghezza;
	}
	public String getLarghezza() {
		return Larghezza;
	}
	public void setLarghezza(String larghezza) {
		Larghezza = larghezza;
	}
	public String getProfondità() {
		return Profondità;
	}
	public void setProfondità(String profondità) {
		Profondità = profondità;
	}

}
